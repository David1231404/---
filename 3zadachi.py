#На дом задано 3 задачи. 
#На решение каждой следующей тратится вдвое меньше времени.
#Измерьте время, затраченное на первую задачу и оцените общее время выполнения ДЗ.

import time

# Время выполнения первой задачи (в секундах)
время_первой_задачи = 3600  # Предположим, что на первую задачу потрачено 1 час (3600 секунд)

# Общее время выполнения всех задач
общее_время = время_первой_задачи

for i in range(1, 3):  # Предполагаем, что всего 3 задачи
    общее_время += время_первой_задачи / (2 ** i)

# Преобразуем общее время в часы и минуты для удобства отображения
часы = int(общее_время // 3600)
минуты = int((общее_время % 3600) // 60)

# Выводим результат
print(f"Время выполнения первой задачи: {время_первой_задачи} секунд")
print(f"Общее время выполнения всех задач: {часы} часов {минуты} минут")
